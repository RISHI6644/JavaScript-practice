/* JavaScript: JavaScript is a high-level (only focused on writing and executing codes;
    Other tasks like memory allocation, memory management is handled behind the scene),
    multi-paradigm (can design procedural, functional as well as object-oriented programs),
    Object-oriented programming language.
    The programs in this language are called scripts. They can be written right in a web
    page’s HTML and run automatically as the page loads.
    Scripts are provided and executed as plain text. They don’t need special preparation or
    compilation to run. In this aspect, JavaScript is very different from another language
    called Java.

    JS is Synchronous (every program will be executed line by line) and single threaded
    (execution of instructions in a single sequence or one command is processes at a time)
    in behavior. But we can achieve multi-threading by applying asynchronous programming.

    Js is the language of the web and is used to make web pages and wen applications work
    dynamically.
 */

console.log("Let's learn about what can JavaScript do on the browser");

/* Almost every browser we see currently is empowered by JS. Because, every browser has an
    embedded JS engine (a.k.a “JavaScript virtual machine”) such as Chrome uses V8 Engine,
    Mozilla Firefox uses spider monkey. Engines works as following:
        i) The engine (embedded if it’s in a browser) reads (“parses”) the script.
        ii) Then it converts (“compiles”) the script to machine code.
        iii) And then the machine code runs, pretty fast.
    The engine applies optimizations at each step of the process.

    In-browser JavaScript can do everything related to webpage manipulation, interaction with
    the user, and the webserver. Additionally, it can do the followings:
        i) Add new HTML to the page, change the existing content, modify styles.
        ii) React to user actions, run on mouse clicks, pointer movements, key presses.
        iii) Send requests over the network to remote servers, download and upload files
        iv) Get and set cookies, ask questions to the visitor, show messages
        v) Remember the data will be stored on the client-side (“local storage”)
 */

